#Escreva um programa que solicite ao usuário o seu salário mensal e o número de meses trabalhados no ano. Calcule e imprima o salário anual.#
salario = int(input('Quanto você ganha?'))
carga = int(input('Quantos meses você trabalha?'))
salarioanual = (salario) * (carga)
print('Seu salario anual é: ',salarioanual,)