#Escreva um programa que solicite ao usuário duas strings e verifique se elas são iguais. Imprima uma mensagem informando o resultado da comparação.#
a = str(input("Verfique se duas palavras sâo iguais, a primeira:"))
b = str(input('A outra palavra:'))
if a == b : print('As palavras são iguais.')
else: print('A palavras não são identicas.')