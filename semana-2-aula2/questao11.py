#Escreva um programa que solicite ao usuário dois números e verifique se o primeiro número é maior que o segundo. Imprima uma mensagem informando o resultado da comparação.#
a = int(input('Verifique se o seu numero é maior que um segundo numero. Primeiro numero:'))
b = int(input(' Segundo numero:'))
if a > b : input( 'O primeiro numero é maior que o segundo')
if a < b : input( 'O primeiro numero é menor que o segundo numero')